    private static void zapiszTemp(String wynik){
        File f = new File("temp.txt");
        try{
            FileWriter fw = new FileWriter(f, true);
            fw.write(wynik);
            fw.close();
        }catch(IOException e){
            System.out.println("BLAD: "+e.toString());
        }
    }
