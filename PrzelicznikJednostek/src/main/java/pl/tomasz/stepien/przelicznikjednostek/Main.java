    private void przeliczTemp(){
        String jednostka = (String)ts_jCBtemperatura.getSelectedItem();
        String daneZle = ts_jTFdane1.getText();
        Double dane = Double.parseDouble(daneZle);
        double C=0;
        double F=0;
        double K=0;
        String wynik;
        System.out.println(jednostka);
        switch(jednostka){
            case "°C":
                C = dane;
                F = 1.8 * dane + 32;
                K = dane + 273.15;
                ts_jTAwynik1.setText("Stopnie celcjusza: "+C+"\n"+"Stopnie Fahrenheita: "+F+"\n"+"Kelwin: "+K);
                wynik = "Stopnie celcjusza: "+C+"\n"+"Stopnie Fahrenheita: "+F+"\n"+"Kelwin: "+K+"\n"+"\n";
                zapiszTemp(wynik);
                break;
            case "°F":
                F = dane;
                C = 0.5555555 * (dane - 32);
                K = (dane + 459.67) * 0.5555555;
                ts_jTAwynik1.setText("Stopnie celcjusza: "+C+"\n"+"Stopnie Fahrenheita: "+F+"\n"+"Kelwin: "+K);
                wynik = "Stopnie celcjusza: "+C+"\n"+"Stopnie Fahrenheita: "+F+"\n"+"Kelwin: "+K+"\n"+"\n";
                zapiszTemp(wynik);
                break;
            case "K":
                K = dane;
                C = dane - 273.15;
                F = (dane * 1.8) - 459.67;
                ts_jTAwynik1.setText("Stopnie celcjusza: "+C+"\n"+"Stopnie Fahrenheita: "+F+"\n"+"Kelwin: "+K);
                wynik = "Stopnie celcjusza: "+C+"\n"+"Stopnie Fahrenheita: "+F+"\n"+"Kelwin: "+K+"\n"+"\n";
                zapiszTemp(wynik);
                break;
            default:
                JOptionPane.showMessageDialog(this, "<html>BŁĄD: Nie wybrano jednostki</html>");
        }
    }
